$('.fa-bars').click(function() {
  $('ul').toggleClass('active');
  $('.toggle').toggleClass('bars-active');
  $('.toggleX').toggleClass('X-active');
  $('body').toggleClass('ScrollLock');
})

$('.fa-times').click(function() {
  $('ul').toggleClass('active');
  $('.toggle').toggleClass('bars-active');
  $('.toggleX').toggleClass('X-active');
  $('body').toggleClass('ScrollLock');
})